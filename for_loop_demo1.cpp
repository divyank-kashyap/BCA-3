// how for loop work with 2 variable (i & j)


#include <iostream>
using namespace std;
int main()
{
    for (int i = 1, j = 0; i <= 5, j < 3; i++, j++) 
    {
        cout << i << " "<< j << endl;
    }

    cout << endl;

    for (int i = 1; i<=5; i++)
    {
        cout << i << endl;
    }

    cout << endl;

    for (int j=0; j<3; j++)
    {
        cout << j <<endl;
    }

}




