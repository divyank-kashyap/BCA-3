// to iterate in an array using for loop
// printing elements of array using for loop

#include <iostream>
using namespace std;
int main()
{
    int arr[5]= {10,20,30,40,50};
    cout << "elements in array: ";
    for (int i=0; i<=4;i++)     
    {
        cout << arr[i] << " ";
    }
}